public class PosterItems {


    private int id;
    private String genreFilm;
    private String filmName;


    public PosterItems(int id, String genreFilm, String filmName) {
        this.id = id;
        this.genreFilm = genreFilm;
        this.filmName = filmName;
    }

    public int getId() {
        return id;
    }

}


